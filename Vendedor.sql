UNION
SELECT nome FROM Vendedores_Filial_MG;

SELECT codigo, nome FROM Vendedores_Matriz
UNION ALL
SELECT codigo, nome FROM Vendedores_Filial_MG;

SELECT M.codigo, M.nome
FROM Vendedores_Matriz AS M
INNER JOIN Vendedores_Filial_MG AS F
ON M.codigo = F.codigo;

SELECT M.codigo, M.nome
FROM Vendedores_Matriz AS M
LEFT JOIN Vendedores_Filial_MG AS F
ON M.codigo = F.codigo
WHERE F.codigo IS NULL;

SELECT F.codigo, F.nome
FROM Vendedores_Filial_MG AS F
LEFT JOIN Vendedores_Matriz AS M
ON F.codigo = M.codigo
WHERE M.codigo IS NULL;
